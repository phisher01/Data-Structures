public class Operators {
    public static void main(String[] args) {
        
        System.out.println(5&6);
        System.out.println(5|6);
        System.out.println(5^6);
        System.out.println(~1);
        System.out.println(~0);
        System.out.println(5<<3);
        System.out.println(6>>2);
        
        
        
    }

     
}